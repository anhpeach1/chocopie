Nhân dịp đầu Xuân, vua mới họp các hoàng tử lại, bảo rằng: “Con nào tìm được thức ăn ngon lành, để bày cỗ cho có ý nghĩa nhất, thì ta sẽ truyền ngôi vua cho”.

Các hoàng tử đua nhau tìm kiếm của ngon vật lạ dâng lên cho vua cha, với hy vọng mình lấy được ngai vàng.

Trong khi đó, người con trai thứ 18 của Hùng Vương, là Tiết Liêu (còn gọi là Lang Liêu) có tính tình hiền hậu, lối sống đạo đức, hiếu thảo với cha mẹ. Vì mẹ mất sớm, thiếu người chỉ vẽ, nên ông lo lắng không biết làm thế nào.

Một hôm, Tiết Liêu nằm mộng thấy có vị Thần đến bảo: “Này con, vật trong Trời Đất không có gì quý bằng gạo, vì gạo là thức ăn nuôi sống con người. Con hãy nên lấy gạo nếp làm bánh hình tròn và hình vuông, để tượng hình Trời và Đất. Hãy lấy lá bọc ngoài, đặt nhân trong ruột bánh, để tượng hình Cha Mẹ sinh thành.”

Tiết Liêu tỉnh dậy, vô cùng mừng rỡ. Ông làm theo lời Thần dặn, chọn gạo nếp thật tốt làm bánh vuông để tượng hình Đất, bỏ vào chõ chưng chín gọi là Bánh Chưng. Và ông giã xôi làm bánh tròn, để tượng hình Trời, gọi là bánh dày. Còn lá xanh bọc ở ngoài và nhân ở trong ruột bánh là tượng hình cha mẹ yêu thương đùm bọc con cái.
